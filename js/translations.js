window.applyLanguage = function() {
    const settings = getSettings();
    const lang = settings.lang || "en"; // Default to English
    
    // Set Document Direction and Language
    document.documentElement.lang = lang;
    document.documentElement.dir = (lang === "ar") ? "rtl" : "ltr";
    
    // Update all elements with data-i18n attribute
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) {
            // If it's an input/textarea, update the placeholder
            if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
                el.placeholder = TRANSLATIONS[lang][key];
            } else {
                // Determine if we should preserve innerHTML like emojis/icons
                // E.g., if there's a span child we maybe only replace text node, 
                // but for now, direct replace is safest. Wait, replacing textContent clears icons.
                // Let's use robust text replacement:
                // Only replace the first text node or use a simple heuristic:
                // If it has children and one is a span, avoid overwriting the span.
                // Better approach: wrap the text to be localized in a <span data-i18n="key"> text </span>
                el.textContent = TRANSLATIONS[lang][key];
            }
        }
    });

    // Fire custom event to notify components that language changed (e.g., re-rendering greetings)
    document.dispatchEvent(new Event("languageChanged"));
};
